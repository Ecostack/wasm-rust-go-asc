#[wasm_bindgen]
pub fn testSort() {
    const length: usize = 100_000;
    let mut arr: [i32; length] = [0; length];
    for i in 0..arr.len() {
        arr[i] = rand::random()
    }

    let mut temp: [i32; length] = [0; length];
    for _i in 0..500 {
        for j in 0..length {
            temp[j] = arr[j]
        }
        temp.sort()
    }
}
